# printing 1 to 5
i = 1
while i <=5:
    print(i,end=' ')
    i += 1
print()


# printing 5 to 1
j = 5
while j >= 1:
    print(j,end=' ')
    j -= 1
print()
    
    
# printing 2 to 10 (even numbers)
k = 2
while k <= 10:
    print(k,end=' ')
    k += 2
print()
    
    
    
# squares of 1 to 5
l = 1
while l <=5:
    print(l*l,end=' ')
    l += 1
print()
    

# printing 1,5  2,4, so on
a,b = 1,5
while a<=5 and b>=1:
    print(a,b)
    a += 1
    b -= 1